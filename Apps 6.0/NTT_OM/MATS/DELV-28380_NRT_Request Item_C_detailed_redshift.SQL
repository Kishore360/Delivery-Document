Select 'VW_SC_REQ_ITEM',Count(1) as CNT 
FROM VW_SC_REQ_ITEM
UNION 
Select 'VW_SC_REQ_ITEM_CATALOG_ITEM',Count(1) as CNT 
FROM VW_SC_REQ_ITEM a
LEFT OUTER JOIN VW_SC_REQ_ITEM_CATALOG_ITEM b ON a.CAT_ITEM=b.sys_id
UNION 
Select 'VW_LOV_CATALOG_ITEM_CATEGORY',Count(1) as CNT 
FROM VW_SC_REQ_ITEM a
LEFT OUTER JOIN VW_SC_REQ_ITEM_CATALOG_ITEM b ON a.CAT_ITEM=b.sys_id 
LEFT OUTER JOIN VW_LOV_CATALOG_ITEM_CATEGORY c ON b.CATEGORY=c.sys_id
UNION 
Select 'VW_LOV_SC_REQ_ITEM_STATE',Count(1) as CNT 
FROM VW_SC_REQ_ITEM a
LEFT OUTER JOIN VW_LOV_SC_REQ_ITEM_STATE b ON a.STATE=b.sys_id 
UNION 
Select 'VW_SC_REQ_ITEM_ASSIGNMENT_GROUP',Count(1) as CNT 
FROM VW_SC_REQ_ITEM a
LEFT OUTER JOIN VW_SC_REQ_ITEM_ASSIGNMENT_GROUP b ON a.assignment_group=b.sys_id 
UNION 
Select 'VW_SC_REQ_ITEM_ASSIGNMENT_GROUP_MANAGER',Count(1) as CNT 
FROM VW_SC_REQ_ITEM a
LEFT OUTER JOIN VW_SC_REQ_ITEM_ASSIGNMENT_GROUP b ON a.assignment_group=b.sys_id 
LEFT OUTER JOIN VW_SC_REQ_ITEM_ASSIGNMENT_GROUP_MANAGER c ON b.MANAGER=c.sys_id 
UNION 
Select 'VW_SC_REQ_ITEM_CLOSED_CALENDAR_DATE',Count(1) as CNT 
FROM VW_SC_REQ_ITEM a
LEFT OUTER JOIN VW_SC_REQ_ITEM_CLOSED_CALENDAR_DATE b ON a.CLOSED_ON_KEY=b.sys_id 
UNION 
Select 'VW_SC_REQ_ITEM_OPENED_CALENDAR_DATE',Count(1) as CNT 
FROM VW_SC_REQ_ITEM a
LEFT OUTER JOIN VW_SC_REQ_ITEM_OPENED_CALENDAR_DATE b ON a.OPENED_ON_KEY=b.sys_id
UNION 
Select 'VW_SC_REQ_ITEM_OPENED_BY',Count(1) as CNT 
FROM VW_SC_REQ_ITEM a
LEFT OUTER JOIN VW_SC_REQ_ITEM_OPENED_BY b ON a.OPENED_BY=b.sys_id 
UNION 
Select 'VW_SC_REQ_ITEM_OPENED_BY',Count(1) as CNT 
FROM VW_SC_REQ_ITEM a
LEFT OUTER JOIN VW_SC_REQ_ITEM_OPENED_BY b ON a.OPENED_BY=b.sys_id 
LEFT OUTER JOIN VW_SC_REQ_ITEM_OPENED_BY_LOCATION c ON b.LOCATION=c.sys_id 