Select 'VW_INCIDENT',Count(1) as CNT 
FROM VW_INCIDENT a
UNION 
Select 'VW_INCIDENT_ASSIGNMENT_GROUP',Count(1) as CNT 
FROM VW_INCIDENT  a
LEFT OUTER JOIN VW_INCIDENT_ASSIGNMENT_GROUP b ON a.assignment_group=b.sys_id
UNION 
Select 'VW_INCIDENT_ASSIGNMENT_GROUP_MANAGER',Count(1) as CNT 
FROM VW_INCIDENT  a
LEFT OUTER JOIN VW_INCIDENT_ASSIGNMENT_GROUP b ON a.assignment_group=b.sys_id
LEFT OUTER JOIN VW_INCIDENT_ASSIGNMENT_GROUP_MANAGER c ON b.MANAGER=c.sys_id 
UNION 
Select 'VW_INCIDENT_CLOSED_CALENDAR_DATE',Count(1) as CNT 
FROM VW_INCIDENT  a
LEFT OUTER JOIN VW_INCIDENT_CLOSED_CALENDAR_DATE b ON a.CLOSED_ON_KEY=b.sys_id
UNION 
Select 'VW_INCIDENT_CMDB_COMPUTER',Count(1) as CNT 
FROM VW_INCIDENT  a
LEFT OUTER JOIN VW_INCIDENT_CMDB_COMPUTER b ON a.CMDB_CI_COMPUTER=b.sys_id
UNION 
Select 'VW_INCIDENT_CONFIGURATION_ITEM',Count(1) as CNT 
FROM VW_INCIDENT  a
LEFT OUTER JOIN VW_INCIDENT_CONFIGURATION_ITEM b ON a.cmdb_ci=b.sys_id
UNION 
SELECT 'VW_INCIDENT_OPENED_CALENDAR_DATE',Count(1) as CNT 
FROM VW_INCIDENT a 
LEFT OUTER JOIN VW_INCIDENT_OPENED_CALENDAR_DATE b ON a.OPENED_ON_KEY=b.SYS_ID
UNION 
SELECT 'VW_INCIDENT_OPENED_BY',Count(1) as CNT 
FROM VW_INCIDENT a 
LEFT OUTER JOIN VW_INCIDENT_OPENED_BY b ON a.opened_by=b.SYS_ID
UNION 
SELECT 'VW_INCIDENT_OPENED_BY_LOCATION',Count(1) as CNT 
FROM VW_INCIDENT a 
LEFT OUTER JOIN VW_INCIDENT_OPENED_BY b ON a.opened_by=b.SYS_ID
LEFT OUTER JOIN VW_INCIDENT_OPENED_BY_LOCATION c ON b.LOCATION=c.sys_id
UNION 
SELECT 'VW_INCIDENT_RESOLVED_CALENDAR_DATE',Count(1) as CNT 
FROM VW_INCIDENT a 
LEFT OUTER JOIN VW_INCIDENT_RESOLVED_CALENDAR_DATE b ON a.RESOLVED_ON_KEY=b.SYS_ID
UNION 
SELECT 'VW_LOV_INCIDENT_CATEGORY',Count(1) as CNT 
FROM VW_INCIDENT a 
LEFT OUTER JOIN VW_LOV_INCIDENT_CATEGORY b ON a.category=b.SYS_ID
UNION 
SELECT 'VW_LOV_INCIDENT_PRIORITY',Count(1) as CNT 
FROM VW_INCIDENT a 
LEFT OUTER JOIN VW_LOV_INCIDENT_PRIORITY b ON a.priority=b.SYS_ID
UNION 
SELECT 'VW_LOV_INCIDENT_STATE',Count(1) as CNT 
FROM VW_INCIDENT a 
LEFT OUTER JOIN VW_LOV_INCIDENT_STATE b ON a.STATE=b.SYS_ID
UNION 
SELECT 'VW_LOV_INCIDENT_SUBCATEGORY',Count(1) as CNT 
FROM VW_INCIDENT a 
LEFT OUTER JOIN VW_LOV_INCIDENT_SUBCATEGORY b ON a.subcategory=b.SYS_ID


