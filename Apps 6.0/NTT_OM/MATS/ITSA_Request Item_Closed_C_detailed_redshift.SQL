FROM ldb.F_REQUEST_ITEM
UNION 
SELECT 'D_REQUEST',Count(1) as CNT 
FROM ldb.F_REQUEST_ITEM a
JOIN ldb.D_REQUEST_ITEM b ON a.REQUEST_ITEM_KEY=b.row_key 
UNION 
SELECT 'D_REQUEST_ITEM_EMPLOYEE',Count(1) as CNT 
FROM ldb.F_REQUEST_ITEM a
JOIN ldb.D_REQUEST_ITEM_EMPLOYEE b ON a.request_item_employee_key=b.row_key 
